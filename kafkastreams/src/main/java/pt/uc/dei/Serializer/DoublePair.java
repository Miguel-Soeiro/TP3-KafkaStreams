package pt.uc.dei.Serializer;

public class DoublePair {
    private Integer first;
    private Integer second;

    public DoublePair(Integer first, Integer second) {
        this.first = first;
        this.second = second;
    }

    public Integer getFirst() {
        return first;
    }

    public Integer getSecond() {
        return second;
    }
}